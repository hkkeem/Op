# a라는 박스에 2를 넣는다.
a = 2
# b라는 박스에 3.14를 넣는다.
b = 3.14
# c라는 박스에 "안녕하세요"를 넣는다.
c = "안녕하세요"

# print라는 함수에  변수 a를 재료로 건네주면 표준출력으로 a에 저장된 값을 출력한다.
print(a)
print(b)
print(c)

# type이라는 함수에 a를 넣어주면 a의 type이 무엇인지 알려준다.
# type_a에 type함수의 결과를 저장한다.
type_a = type(a)
type_b = type(b)
type_c = type(c)

# print라는 함수를 이용하여 type_a type_b type_c의 값을 출력한다.
print(type_a)
print(type_b)
print(type_c)
